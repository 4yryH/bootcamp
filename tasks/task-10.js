/**
 1. Напиши функцию, которая принимает строку и проверяет, является ли она палиндромом.
 Если да - функция возвращает true, если нет - false;
 */

/* Попробую убрать пробелы, приведу к одному регистру,
приведу строку к массиву, создам дубликат, разверну и сравню между собой по элементам
 */

const checkPalindrome = (someText) => {
  // убираю пробелы и привожу к нижнему регистру
  const textWithoutSpaces = someText.replace(/\s+/g, '').toLowerCase();
  // разбиваю на массив
  const textToArray = textWithoutSpaces.split('');
  // делаю дубликат
  const textDuplicate = [...textToArray];
  // разворачиваю дубликат
  const textReversed = textDuplicate.reverse();
  // сравнение по элементам с перебором через цикл
  for (let i = 0; i < textToArray.length; i++) {
    if (textToArray[i] !== textReversed[i]) {
      return false
    }
  }
  return true;
}

console.log(checkPalindrome('А роза упала на лапу Азора'));
console.log(checkPalindrome('Шла Cаша по шоссе'));
console.log(checkPalindrome('Анна'));

/**
 2. Напиши функцию, которая принимает строку (предложение)
 и находит первое самое короткое слово в ней и возвращает его;
 */
//Разбить на массив слов, отсортировать по длине и вернуть первое значение
const getShortWord = (text) => {
  const wordsArray = text.split(' ');
  const sortArray = wordsArray.sort((a, b) => a.length - b.length);

  return sortArray[0];
}

console.log(getShortWord('Вот о и лето а ж пришло'))


/**
 4. Напиши функцию, которая ищет минимальное и максимальное значение в массиве;
 */
// отсортировать массив от меньшего к большему и вернуть первый и последний элемент
const getMaxMin = (arr) => {
  const arrSorted = arr.sort((a, b) => a - b);
  return {
    min: arrSorted[0],
    max: arrSorted[arrSorted.length - 1],
  };
}

console.log(getMaxMin([18, 9, 1, 13, 4]));


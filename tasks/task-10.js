/* Bubble sort - сравнивание элементов,
 Нужна вспомогательная переменная. Если сразу записать на первое место значение второго элемента,
 то мы потеряем значение первого элемента. Поэтому сначала нужно значение первого элемента сохранить в переменную.
 Добавим переменную `minValue` для хранения минимального значения.
 Предположим, что первый элемент и есть минимальный. Поэтому до цикла сохраним в `minValue` значение первого элемента.
 На каждой итерации цикла сравниваем текущий элемент со значением `minValue`.
 Если текущий элемент меньше `minValue`, то записываем его в `minValue`
 Если первый элемент и был минимальный, то в цикле значение `minValue` не изменится,
 если же после первого элемента были элементы с меньшим значением, то это значение запишется в `minValue` в цикле.
 В любом случае минимальный элемент будет найден.
 На каждой итерации цикла сравниваем текущий элемент со значением `minValue`
 Если текущий элемент меньше `minValue`, то записываем его в `minValue`,
 а затем меняем местами значение первого элемента и текущего.
 После завершения работы цикла на первой позиции массива окажется элемент с минимальным значением.
 Все остальные элементы будут больше.
 Далее аналогично перебираем оставшиеся элементы массива и ставим их по порядку

 В двух словах: мы берем элемент и записываем его в переменную.
 Сравниваем эту переменную с каждым элементом, если найдется элемент меньше, чем в переменной,
 то его запишем в переменную, если нет такого элемента в массиве, то у нас в переменной хранится
 наименьший элемент. Запишем его в начало массива и так далее
 */

let numbers = [1945, 42, 4242, 197, 365];
console.log(numbers);

for (let i = 0; i <= numbers.length - 2; i++) {
  let minValue = numbers[i];

  for (let j = i + 1; j <= numbers.length - 1; j++) {
    if (numbers[j] < minValue) {
      minValue = numbers[j];
      let swap = numbers[i];
      numbers[i] = minValue;
      numbers[j] = swap;
    }
  }
}

console.log(numbers)

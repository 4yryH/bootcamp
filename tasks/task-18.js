/**
 3*. –ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É–π –∏ —Ä–∞–∑–±–µ—Ä–∏ —Ñ—É–Ω–∫—Ü–∏—é –≥–ª—É–±–æ–∫–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è –∏–∑ —É—Ä–æ–∫–∞ (function deepCopy).
 –ü–æ–∫—Ä–æ–π –∫–∞–∂–¥—É—é —Å—Ç—Ä–æ—á–∫—É –∫–æ–¥–∞ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è–º–∏, –∫–æ—Ç–æ—Ä—ã–µ –æ–±—ä—è—Å–Ω—è—é—Ç, —á—Ç–æ –¥–µ–ª–∞–µ—Ç —ç—Ç–æ—Ç –∫—É—Å–æ–∫ –∫–æ–¥–∞ –∏ –∑–∞—á–µ–º.
 –¢–æ –µ—Å—Ç—å –ø–æ—Å–ª–µ —ç—Ç–æ–≥–æ –∑–∞–¥–∞–Ω–∏—è —É —Ç–µ–±—è –¥–æ–ª–∂–Ω–æ —Å–ª–æ–∂–∏—Ç—å—Å—è –ø–æ–ª–Ω–æ–µ –ø–æ–Ω–∏–º–∞–Ω–∏–µ —Ç–æ–≥–æ, –∫–∞–∫ —Ä–∞–±–æ—Ç–∞–µ—Ç —ç—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è,
 —á—Ç–æ–±—ã –≤ –¥–∞–ª—å–Ω–µ–π—à–µ–º —Ç—ã —Å–º–æ–≥ –µ–µ –ø—Ä–∏–º–µ–Ω–∏—Ç—å!

 - –∑–∞–¥–∞–Ω–∏–µ —Ç—Ä–µ–±—É–µ—Ç —Å–∞–º–æ—Å—Ç–æ—è—Ç–µ–ª—å–Ω–æ–≥–æ –∏–∑—É—á–µ–Ω–∏—è —Ç–µ–º—ã, –∫–æ—Ç–æ—Ä—É—é –º—ã –µ—â–µ –Ω–µ –ø—Ä–æ—Ö–æ–¥–∏–ª–∏ - —Ä–µ–∫—É—Ä—Å–∏–∏ + –≥—É–≥–ª–∏–Ω–≥ –Ω–µ–∑–Ω–∞–∫–æ–º—ã—Ö –º–µ—Ç–æ–¥–æ–≤.

 */

//—Å–æ–∑–¥–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞ —Å–µ–±—è –ø—Ä–∏–Ω–∏–º–∞–µ–º –∫–∞–∫–æ–π-—Ç–æ –æ–±—ä–µ–∫—Ç
function deepCopy(obj) {
  // –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö –Ω–∞ null –∏ –¥—Ä—É–≥–∏–µ —Ç–∏–ø—ã –¥–∞–Ω–Ω—ã—Ö, –ø—Ä–∏–º–∏—Ç–∏–≤—ã,
  // –Ω–∞ null –ø—Ä–æ–≤–µ—Ä—è–µ–º –æ—Ç–¥–µ–ª—å–Ω–æ, –ø–æ—Ç–æ–º—É —á—Ç–æ —ç—Ç–æ –∂ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ
  // –¥—Ä—É–≥–∏–µ —Ç–∏–ø—ã –¥–∞–Ω–Ω—ã—Ö, –Ω–∞–ø—Ä–∏–º–µ—Ä, –º–æ–∂–µ—Ç –Ω–∞ –≤—Ö–æ–¥–µ –ø—Ä–æ—Å—Ç–æ
  // –ø—Ä–∏—à–ª–æ —á–∏—Å–ª–æ –∏–ª–∏ —Å—Ç—Ä–æ–∫–∞, –∑–Ω–∞—á–∏—Ç –º—ã –µ–µ –∂–µ –∏ –≤–µ—Ä–Ω–µ–º –æ–±—Ä–∞—Ç–Ω–æ
  if (obj === null || typeof obj !== 'object') {
    // –µ—Å–ª–∏ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, —Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–±—Ä–∞—Ç–Ω–æ, –µ—Å–ª–∏ –≤—Å–µ —Ç–∞–∫–∏ –æ–±—ä–µ–∫—Ç, —Ç–æ –∏–¥–µ–º –¥–∞–ª—å—à–µ
    return obj;
  }
  // —Å–æ–∑–¥–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é, –≤ –∫–æ—Ç–æ—Ä—É—é –∏ –±—É–¥–µ–º –∫–æ–ø–∏—Ä–æ–≤–∞—Ç—å, —Ç–∞–∫ –∂–µ –ø—Ä–æ–≤–µ—Ä–∏–º –º–∞—Å—Å–∏–≤ –∏–ª–∏ –æ–±—ä–µ–∫—Ç,
  // –º–∞—Å—Å–∏–≤ —Ç–æ–∂–µ —Å–≤–æ–µ–≥–æ —Ä–æ–¥–∞ —è–≤–ª—è–µ—Ç—Å—è –æ–±—ä–µ–∫—Ç–æ–≤ –≤ JS, –Ω–æ –Ω–µ–º–Ω–æ–≥–æ —Å –¥—Ä—É–≥–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–π,
  // –∏–º–µ–µ—Ç —É–ø–æ—Ä—è–¥–æ—á–µ–Ω–Ω–æ—Å—Ç—å, –¥–ª–∏–Ω—É –∏ –¥—Ä—É–≥–∏–µ –º–µ—Ç–æ–¥—ã —Ä–∞–±–æ—Ç—ã
  const copy = Array.isArray(obj) ? [] : {};
  // –ø—Ä–æ–±–µ–≥–∞–µ–º –ø–æ –∫–ª—é—á–∞–º —á–µ—Ä–µ–∑ —Ü–∏–∫–ª
  for (let key in obj) {
    // –ø—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–Ω–æ—Å—Ç—å —Å–∞–º–æ–º—É –æ–±—ä–µ–∫—Ç—É
    if (obj.hasOwnProperty(key)) {
      // –ß—Ç–æ –±—ã –ø–æ–Ω—è—Ç—å —Ä–µ–∫—É—Ä—Å–∏—é, –Ω—É–∂–Ω–æ –ø–æ–Ω—è—Ç—å —Ä–µ–∫—É—Ä—Å–∏—é))
      // –∑–¥–µ—Å—å –º—ã –≤—ã–∑—ã–≤–∞–µ–º —Ä–µ–∫—É—Ä—Å–∏—é –∏ –±—É–¥–µ–º —Ö–æ–¥–∏—Ç—å –ø–æ –∫—Ä—É–≥—É, –∫–æ–ø–∏—Ä—É—è –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏ –Ω–∞ –≤—Å—é –≥–ª—É–±–∏–Ω—É
      // —Ç.–µ. –º—ã –∏–¥–µ–º –ø–æ –ø–µ—Ä–≤–æ–º—É –∫—Ä—É–≥—É, –∑–∞–±–∏—Ä–∞–µ–º –¥–∞–Ω–Ω—ã–µ, –∏–¥–µ–º –≤–æ –≤—Ç–æ—Ä–æ–º—É –∫—Ä—É–≥—É –∏ –¥–æ—Ö–æ–¥–∏–º –¥–æ –ø—Ä–∏–º–∏—Ç–∏–≤–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö,
      // –Ω–∞ –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–æ–≤–µ—Ä—è–µ–º –≤ —Å–∞–º–æ–º –Ω–∞—á–∞–ª–µ, –∏–ª–∏ –∂–µ –∏–¥–µ–º –µ—â–µ –Ω–∞ –∫—Ä—É–≥,
      // –ø–æ–∫–∞ –Ω–µ –¥–æ–π–¥–µ–º –¥–æ —Ç–µ—Ö —Å–∞–º—ã—Ö –¥–∞–Ω–Ω—ã—Ö, –∏–º–µ–Ω–Ω–æ —Ç–∞–∫ –∏ –±—É–¥—É—Ç –ø—Ä–æ–π–¥–µ–Ω—ã –≤—Å–µ —É—Ä–æ–≤–Ω–∏ –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏
      copy[key] = deepCopy(obj[key]);
    }
  }
  // –ù—É–∏ —Å–∞–º –≤–æ–∑–≤—Ä–∞—Ç –Ω–∞—à–µ–π –∫–æ–ø–∏–∏
  return copy;
}


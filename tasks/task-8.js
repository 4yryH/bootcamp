  // –≤ –ø—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤ –±—É–¥–µ—Ç –¥–æ–±–∞–≤–ª—è—Ç—å –≤ –Ω–∞—á–∞–ª–æ –º–∞—Å—Å–∏–≤–∞ –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ –ø–µ—Ä–µ–±–æ—Ä–∞

/**
 4. –ù–∞–ø–∏—à–∏ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —Ä–∞–∑–≤–µ—Ä–Ω–µ—Ç –º–∞—Å—Å–∏–≤, —Ç–æ –µ—Å—Ç—å –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –±—É–¥—É—Ç –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.
 –ù–µ–ª—å–∑—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å reverse()!
 */

const seasons = ['summer', 'autumn', 'winter', 'spring']; // –∏—Å—Ö–æ–¥–Ω—ã–π –º–∞—Å—Å–∏–≤
const seasonsReversed = []; // –ø—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤ –¥–ª—è –¥–∞–ª—å–Ω–µ–π—à–µ–≥–æ –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏

